int main(int argc, char *argv[])
{
	pid_t pid;
	
	printf("start fork\n");
	
	pid = fork();
	
	if (!pid) {
		printf("child start\n");
	} else if (0 < pid) {
		printf("parent is returned\n");
	} else {
		printf("fork failed\n");
	}

	printf("fork example is finished!\n");
	
	return(pid);
}


